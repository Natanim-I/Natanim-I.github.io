const p1Images = document.querySelectorAll(".pr1-img");

p1Images.forEach((img) => {
  img.addEventListener("mouseenter", () => {
    img.style.opacity = "0";
  });
  img.addEventListener("mouseleave", () => {
    img.style.opacity = "";
  });
});

